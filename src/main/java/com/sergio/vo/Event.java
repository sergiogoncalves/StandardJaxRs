package com.sergio.vo;

public class Event {
    private int id;
    private String title;
    private int created;
 
    public int getId() {
        return id;
    }
 
    public void setId(int id) {
        this.id = id;
    }
 
    public String getTitle() {
        return title;
    }
 
    public void setTitle(String title) {
        this.title = title;
    }
 
    public int getCreated() {
        return created;
    }
 
    public void setCreated(int created) {
        this.created = created;
    }
}